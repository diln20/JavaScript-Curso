
//sentencias switch

let nota = 5;


switch (nota) {
    case 1:
        console.log("Muy malo");
        break;
    case 2:
        console.log("Malo");
        break;
    case 3:
        console.log("Regular");
        break;
    case 4:
        console.log("Bueno");
        break;
    case 5:
        console.log("Muy bueno");
        break;
    default:
        console.log("Nota no valida");
        break;
}